"""
Prepare CoralSCOP Training Data
=================================
Generates CoralSCOP training data using point prompts.

For each CoralNet annotation point (row, col, species):
    Feed the point as a foreground prompt directly to CoralSCOP
    CoralSCOP generates a mask for the coral colony at that point
    Label = species from the annotation

This produces one labelled mask per annotation point (~46,000 total)
with guaranteed coral content — far better than automatic mode which
segments everything and rarely captures annotated colonies.

Output:
    outputs/coralscop_training_data/
        training_samples.csv     one row per labelled mask
        label_stats.txt          how many samples per species

Usage from the repository root:
    conda activate coralnet10
    python src/patches/extract_coralscop.py

Default local paths:
    outputs/split_manifest.csv          manifest from src/data/build_manifest.py
    models/vit_b_coralscop.pth          CoralSCOP weights, externally downloaded
    external/CoralSCOP/                 optional local CoralSCOP repo clone

All defaults can be overridden with command-line arguments.

This takes ~2-3 hours for 12,859 images. It only needs to run once.
The output CSV is then used by finetune_coralscop_v2.py for training.
"""

