"""
SAM2 Point-Prompt Patch Extractor
===================================
Generates Option A patches using SAM2 point prompts.

For each CoralNet annotation point (image, row, col, species):
    1. Feed (row, col) as SAM2 foreground point prompt
    2. SAM2 generates a mask for that specific coral colony
    3. Extract bbox crop with background zeroed (Option A style)
    4. Save patch with species label

Speed: ~0.3s per annotation with caching -> ~8-10 hours for 240k annotations
Resumable: tracks progress in checkpoint file, safe to interrupt and restart

Usage from the repository root:
    conda activate coralnet10
    python src/patches/extract_sam2.py

Default local paths:
    outputs/split_manifest.csv          manifest from src/data/build_manifest.py
    models/sam2.1_hiera_base_plus.pt    SAM2 weights, externally downloaded
    external/sam2/                      optional local SAM2 repo clone
    outputs/patches_sam2/               generated patches

All defaults can be overridden with command-line arguments.
"""

